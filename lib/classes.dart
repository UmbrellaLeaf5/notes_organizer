/// Представляет текстовую заметку с заголовком и содержимым
///
/// ### Пример создания:
/// ```dart
/// final note = Note(title: 'Покупки', text: 'Молоко, Хлеб');
/// ```
///
/// ### Особенности:
/// - Заголовок обязателен
/// - Текст может быть пустым
/// - Автоматически обрезает пробелы в заголовке
///
/// Смотри также:
/// - [NotePreview] – виджет для отображения заметки
class Note {
  /// Заголовок заметки (не может быть пустым)
  String title;

  /// Основное содержимое заметки (может быть пустым)
  String text;

  Note({required this.title, this.text = ""});
}
